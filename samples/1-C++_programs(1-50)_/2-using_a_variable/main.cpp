// Программа №2 - Использование переменной.

#include <iostream>
using namespace std;
int main()
{
	int x; // Здесь объявляется переменная.
	x = 1023; // Здесь переменной x присваивается число 1923.
	cout << "This program prints the value of x: ";
	cout << x; // Отображение числа 1923.
	
	return 0;
}
